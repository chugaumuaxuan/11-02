
public class DatatypeInSwitchCase {
	
	
	public static void main(String[] args) {
		
		int i = 2;
		switch(i) {
		case 2: System.out.println("day la thu hai");
		}
		
		char a = 2;
		switch(a) {
		case 2: System.out.println("day la thu hai");
		}
		
		byte b = 2;
		switch(b) {
		case 2: System.out.println("day la thu hai");
		}
		
		short c = 2;
		switch(c) {
		case 2: System.out.println("day la thu hai");
		}
		
		String name = "anh";
		switch(name) {
		case "anh": System.out.println("ten la anh");
		}
		
	}
}
