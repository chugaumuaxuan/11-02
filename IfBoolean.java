
public class IfBoolean {
	public static void main(String[] args) {
		
		boolean a = true;
		boolean b = false;
		
		if(a) {
			System.out.println("Hello World");
		}
		if(b) {
			System.out.println("Xin chao");
		}
		if( a == b) {
			System.out.println("hai bieu thuc giong nhau");
		}
		if ( a != b) {
			System.out.println("hai bieu thuc khac nhau");
		}
	}
	
}
